# --- Creating a list ---
my_list = ["Kumar", "Ramesh", "Raju", "Rohan", "Pawan"]
print("Original list:", my_list)

# --- Append ---
my_list.append("Rajesh")  # adds element to the end
print("After append:", my_list)

# --- Insert ---
my_list.insert(1, "Gaurav")  # adds element at index 1
print("After insert:", my_list)

# --- Pop ---
my_list.pop(2)  # removes element using index
print("After pop:", my_list)

# --- Remove ---
my_list.remove("Kumar")  # removes element by value
print("After remove:", my_list)

# --- Sort ---
my_list.sort()  # sorts alphabetically
print("After sort:", my_list)

# --- Reverse ---
my_list.reverse()  # reverses the list
print("After reverse:", my_list)











        

        
    
